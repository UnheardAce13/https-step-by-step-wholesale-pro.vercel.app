'use client'

import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: 'default' | 'glass' | 'quantum' | 'premium' | 'elevated'
  }
>(({ className, variant = 'default', ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow",
    glass: "rounded-xl backdrop-blur-md bg-white/10 border border-white/20 text-white shadow-xl",
    quantum: "rounded-xl bg-gradient-to-br from-blue-500/20 to-purple-500/20 backdrop-blur-sm border border-white/10 shadow-2xl",
    premium: "rounded-xl bg-gradient-to-br from-yellow-500/20 to-orange-500/20 backdrop-blur-sm border border-yellow-500/30 shadow-2xl",
    elevated: "rounded-xl bg-card border shadow-2xl hover:shadow-3xl transition-all duration-300 hover:scale-[1.02]"
  }
  
  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

